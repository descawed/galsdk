def test_bad_raw_sector():
    with pytest.raises(ValueError):
        Sector(b'bogus')


def test_copy_sector():
    source = Sector(minute=1, second=2, sector=3, mode=2)
    # fill in some dummy data
    data = source.data
    for i in range(len(data)):
        data[i] = i & 0xff
    destination = Sector(minute=4, second=5, sector=6, mode=2)

    assert destination.data != source.data
    destination.copy(source)
    assert destination.minute == 4
    assert destination.second == 5
    assert destination.sector == 6
    assert destination.data == source.data
